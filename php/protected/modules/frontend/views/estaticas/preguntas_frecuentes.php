                        <h2 >1- ¿Qué es la alícuota?</h2>
                        <p>Son la parte/porción de un valor, ya sea el del 0km o una parte de él. Existen 2 tipos de "alícuotas" conocidas, una es la que corresponde al 30% y se denomina alícuota extraordinaria, y es la que se entrega sin ningún tipo de intereses al momento de adjudicar el plan y equivale al 30% del valor móvil del 0Km (En un plan 70/30, en un 60/40 será el 40% y así respectivamente según el plan). Y otra denominada alícuota es lo que también se conoce como "cuota pura" o "cuota sin gastos", que es el valor del vehículo 0km, dividido en 84 cuotas del plan sin intereses ni gastos adicionales. En el caso del 70/30 es el 70% del valor del vehículo dividido 84 cuotas. En el plan 100% es el total del vehículo dividido 84 cuotas.</p>
                    </div>
                    <div id="plan100">


                    <h2>2- ¿Qué es un plan 100%?</h2>
                    <p>Un plan 100% o plan tradicional es un Plan de ahorro que se calcula la cuota sobre el valor móvil total del vehículo 0km, y es dividido en 84 cuotas sin interés, obteniendo un valor de alícuota (cuota pura) que no incluye gastos, impuestos ni agregados.</p>
                    </div>
                    <h2 id="plan70-30">3- ¿Qué es un plan 70/30?</h2>
                    <p>Un plan 70/30 se financia el 70% de la unidad en 84 cuotas sin interés y el 30% restante se abona en efectivo (o se pacta con la agencia una entrega de un vehículo usado). En el caso que el vehículo usado supere el 30%, se utiliza el dinero excedente para cancelar los prorrateos y/o cuotas a valor de cuota pura (alícuota). Este plan es ideal para aquellos que disponen de un vehículo ó un monto de dinero y desean cambiarlo por un 0km; como para quien tiene o puede tener, un ahorro paralelo a la cuota mensual, o simplemente para aquél que prefiere abonar una cuota reducida en un 30%.</p>



                    <h2 id="entrega">4- ¿Se puede entregar un auto usado?</h2>
                    <p>La entrega de un usado se pacta con la agencia que realizará la entrega del 0km transformando el valor de toma en efectivo para los pagos que se acuerden, ya sean restar cuotas, pagar gastos de entrega o adelantar cuotas y/o prorrateos.</p>

                    <h2 id="demora">5- ¿Cómo es la demora en la entrega?</h2>
                    <p>La responsabilidad de entrega es de la terminal administradora, y puede gestionarse en cualquier concesionario oficial del país. La misma puede variar según el modelo elegido, su equipamiento y color según la disponibilidad de stock. Normalmente las entregas se realizan dentro de los 30-60 días del realizado el pedido y presentada la documentación requerida. Se pueden adicionar días en el caso que se pida un cambio de modelo a versiones o modelos diferentes al suscripto.</p>
                    <h2 id="modeo">6- ¿Se puede pedir un modelo diferente del suscripto?</h2>
                    <p>Una vez adjudicado el plan se procede a realizar el pedido de la unidad, en el caso que se quiera pedir un modelo diferente al suscripto originalmente en el plan de ahorro, se deberá abonar la diferencia que exista, algunas administradoras permiten financiar este diferencia por el cambio, y otras piden que sea en un solo pago.</p>
                    <h2 id="gastos">7 -¿Existen gastos de retiro?</h2>
                    <p>Si, al retirar se deben abonar gastos como ser: formularios de patentamiento, inscripción de prenda, flete y seguro transporte, informes comerciales,derecho de adjudicación, etc pudiendo diferir según cada marca. Los mismos se abonan al retirar la unidad 0km del concesionario. Todos los costos serán calculados en base a las distancias requeridas, la ubicación del cliente según la provincia y el valor de los impuestos y sellos correspondientes.</p>
